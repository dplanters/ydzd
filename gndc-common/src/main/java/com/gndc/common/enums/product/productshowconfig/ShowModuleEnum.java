public enum ShowModuleEnum {

    //展示模块 1：首页-精选爆款；5：找贷款；15：极速贷；
    HOME((byte)1,"首页-精选爆款"),
    ZHAO_DAI_KUAN((byte)2,"找贷款"),
    JI_SU_DAI((byte)3,"极速贷");

    private Byte code;
    private String name;

    ShowModuleEnum(Byte code, String name) {
        this.code = code;
        this.name = name;
    }

    public Byte getCode() {
        return code;
    }

    public void setCode(Byte code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
